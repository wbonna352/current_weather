CREATE TABLE IF NOT EXISTS weather (
    location VARCHAR(100),
    timestamp TIMESTAMP,
    temp INT,
    temp_unit VARCHAR(20),
    real_feel INT,
    real_feel_shade INT,
    phrase VARCHAR(200),
    wind_direction VARCHAR(20),
    wind_speed INT,
    wind_speed_unit VARCHAR(20),
    wind_gusts_speed INT,
    wind_gusts_speed_unit VARCHAR(20),
    humidity_pct INT,
    cloud_cover_pct INT,
    indoor_humidity_pct INT,
    indoor_humidity_desc VARCHAR(200),
    dew_point INT,
    dew_point_unit VARCHAR(20),
    pressure_sign VARCHAR(20),
    pressure INT,
    pressure_unit VARCHAR(20),
    visibility INT,
    visibility_unit VARCHAR(20),
    cloud_ceiling INT,
    cloud_ceiling_unit VARCHAR(20),
    max_uv_index INT,
    max_uv_index_desc VARCHAR(200)
)